Future<void> main() async {
  WidgetsFlutterBinding.ensureInitialized();

  // Initialize Firebase
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );

  // ‚ö†Ô∏è Initialize global error handling with Crashlytics
  ErrorHandler.initialize();
  
  // üî• Enable Crashlytics collection (set to false for debug builds if needed)
  FirebaseCrashlytics.instance.setCrashlyticsCollectionEnabled(true);

  runApp(const NabdAlHayahApp());
}

class NabdAlHayahApp extends StatefulWidget {
  const NabdAlHayahApp({super.key});

  @override
  State<NabdAlHayahApp> createState() => _NabdAlHayahAppState();
}

class _NabdAlHayahAppState extends State<NabdAlHayahApp> {
  ThemeMode _themeMode = ThemeMode.system;

  @override
  void initState() {
    super.initState();
    _loadThemeMode();
  }

  Future<void> _loadThemeMode() async {
    final savedThemeMode = await AppTheme.getSavedThemeMode();
    setState(() {
      _themeMode = savedThemeMode;
    });
  }

  void updateThemeMode(ThemeMode themeMode) {
    setState(() {
      _themeMode = themeMode;
    });
    AppTheme.saveThemeMode(themeMode);
  }

  @override
  Widget build(BuildContext context) {
    return MultiProvider(
      providers: [
        // üöÄ Auth Provider for global authentication state management
        ChangeNotifierProvider(
          create: (_) => custom_auth.AuthProvider(),
        ),
        // üöÄ Dashboard Provider for navigation state management
        ChangeNotifierProvider(
          create: (_) => DashboardProvider(),
        ),
        // üöÄ Home Provider for home screen state management
        ChangeNotifierProvider(
          create: (_) => HomeProvider(),
        ),
        // üöÄ Activity Provider for activity screen state management
        ChangeNotifierProvider(
          create: (_) => ActivityProvider(),
        ),
        // üöÄ Food Logging Provider for food logging screen state management
        ChangeNotifierProvider(
          create: (_) => FoodLoggingProvider(),
        ),
        // üöÄ Profile Provider for profile screen state management
        ChangeNotifierProvider(
          create: (_) => ProfileProvider(),
        ),
        // üöÄ Global Step Counter Provider for real-time step counting across all screens
        ChangeNotifierProvider(
          create: (_) => GlobalStepCounterProvider(),
        ),
        // Add more providers here as needed
      ],
      child: MaterialApp(
        title: 'Nabd Al-Hayah',
        debugShowCheckedModeBanner: false,
        theme: AppTheme.lightTheme,
        // üé® Removed dark theme - using light theme only
        themeMode: ThemeMode.light, // Always use light theme
        home: ThemeModeProvider(
          updateThemeMode: updateThemeMode,
          child: const RootScreen(),
        ),
      ),
    );
  }
}

class RootScreen extends StatefulWidget {
  const RootScreen({super.key});

  @override
  State<RootScreen> createState() => _RootScreenState();
}

class _RootScreenState extends State<RootScreen> {
  bool _hasShownStartPage = false;

  @override
  Widget build(BuildContext context) {
    return StreamBuilder<User?>(
      stream: FirebaseAuth.instance.authStateChanges(),
      builder: (context, snapshot) {
        // Still loading ‚Üí show splash
        if (snapshot.connectionState == ConnectionState.waiting) {
          return const SplashScreen();
        }

        // User is logged in ‚Üí initialize step counter and go to main navigation
        if (snapshot.hasData) {
          return const MainNavigationWithStepCounter();
        }

        // User not logged in ‚Üí show start page first, then login
        if (!_hasShownStartPage) {
          return const StartPage();
        }
        return const LoginScreen();
      },
    );
  }
}

/// ‚úÖ Provider widget for theme mode changes
class ThemeModeProvider extends InheritedWidget {
  const ThemeModeProvider({
    super.key,
    required this.updateThemeMode,
    required Widget child,
  }) : super(child: child);

  final void Function(ThemeMode) updateThemeMode;

  static ThemeModeProvider? of(BuildContext context) {
    return context.dependOnInheritedWidgetOfExactType<ThemeModeProvider>();
  }

  @override
  bool updateShouldNotify(covariant ThemeModeProvider oldWidget) {
    return updateThemeMode != oldWidget.updateThemeMode;
  }
}

/// Wrapper widget that initializes global step counter before showing main navigation
class MainNavigationWithStepCounter extends StatefulWidget {
  const MainNavigationWithStepCounter({super.key});

  @override
  State<MainNavigationWithStepCounter> createState() => _MainNavigationWithStepCounterState();
}

class _MainNavigationWithStepCounterState extends State<MainNavigationWithStepCounter> {
  @override
  void initState() {
    super.initState();
    // Initialize global step counter when user is logged in
    WidgetsBinding.instance.addPostFrameCallback((_) {
      _initializeGlobalStepCounter();
    });
  }

  Future<void> _initializeGlobalStepCounter() async {
    try {
      final globalStepCounter = context.read<GlobalStepCounterProvider>();
      await globalStepCounter.initialize();
      
      // Connect all providers to global step counter
      final homeProvider = context.read<HomeProvider>();
      homeProvider.setGlobalStepCounter(globalStepCounter);
      
      final dashboardProvider = context.read<DashboardProvider>();
      dashboardProvider.setGlobalStepCounter(globalStepCounter);
      
      final profileProvider = context.read<ProfileProvider>();
      profileProvider.setGlobalStepCounter(globalStepCounter);
      
      final activityProvider = context.read<ActivityProvider>();
      activityProvider.setGlobalStepCounter(globalStepCounter);
      
      debugPrint('Global step counter initialized and connected to all providers successfully');
    } catch (e) {
      debugPrint('Failed to initialize global step counter: $e');
      // Don't prevent app from working if step counter fails
    }
  }

  @override
  Widget build(BuildContext context) {
    return const MainNavigation();
  }
}
